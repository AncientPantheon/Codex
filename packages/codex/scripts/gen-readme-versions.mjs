// Regenerate the "Bundled member versions" table in this package's README from
// the members' own package.json versions, so the aggregator README can never
// drift from what it actually bundles. Idempotent: rewrites only the block
// between the two HTML marker comments.
import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const pkgRoot = resolve(here, "..");
const members = [
  ["@ancientpantheon/codex-core", "../codex-core"],
  ["@ancientpantheon/codex-ui", "../codex-ui"],
  ["@ancientpantheon/codex-ouronet", "../codex-ouronet"],
  ["@ancientpantheon/codex-arweave", "../codex-arweave"],
  ["@ancientpantheon/arweave-core", "../arweave-core"],
];

const rows = members.map(([name, rel]) => {
  const v = JSON.parse(readFileSync(resolve(pkgRoot, rel, "package.json"), "utf8")).version;
  return `| \`${name}\` | \`${v}\` |`;
});
const table = [
  "| Member package | Version |",
  "| --- | --- |",
  ...rows,
].join("\n");

const BEGIN = "<!-- BEGIN member-versions (generated by scripts/gen-readme-versions.mjs) -->";
const END = "<!-- END member-versions -->";
const readmePath = resolve(pkgRoot, "README.md");
let readme = readFileSync(readmePath, "utf8");
const block = `${BEGIN}\n${table}\n${END}`;
// Escape regex metacharacters in the literal markers (they contain `(`, `)`, `.`).
const esc = (s) => s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
const re = new RegExp(`${esc(BEGIN)}[\\s\\S]*?${esc(END)}`, "g");
readme = re.test(readme)
  ? readme.replace(new RegExp(`${esc(BEGIN)}[\\s\\S]*?${esc(END)}`, "g"), block)
  : readme.trimEnd() + "\n\n" + block + "\n";
// Collapse any accidental duplicate blocks left by an earlier unescaped run.
readme = readme.replace(new RegExp(`(${esc(block)}\\s*){2,}`, "g"), block + "\n");
writeFileSync(readmePath, readme, "utf8");
console.log("[gen-readme-versions] updated member-versions table");
